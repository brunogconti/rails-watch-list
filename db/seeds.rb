uri = URI("http://tmdb.lewagon.com/movie/top_rated")
response = Net::HTTP.get(uri)
movies_hash = JSON.parse(response)
movies_array = movies_hash["results"]

movies_array.each do |movie|
  movie_title = movie["title"]
  movie_overview = movie["overview"]
  movie_poster_url = "https://www.themoviedb.org/t/p/w600_and_h900_bestv2#{movie['poster_path']}"
  movie_rating = movie["vote_average"]

  new_movie = Movie.new(title:  movie_title, overview: movie_overview, poster_url: movie_poster_url, rating: movie_rating)
  new_movie.save!
  puts "#{new_movie.id} was created"
end
